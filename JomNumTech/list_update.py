
# list of Integers 
a = list((1,2,3,4,5))
print(a)

# List of String
b = list(('apple','banana','coconut'))
print(b)

# Mixed data type
c = list((2,'jabs',10.58,False))
print(c)

# Repeated Elements with list
d = [1] * 2
print(d)

# Accessing List Elements
e = ['a','e','i','o','u']
print(e[0])
print(e[-2])
print(e[0:4]) # elements from index a -> o

# Update elements by accessing them vai their index.
a = [1,2,3,4,5]
a[2] = 10
print(a)